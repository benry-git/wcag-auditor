#!/usr/bin/env node
/**
 * gen-axe-mapping.mjs - regenerate references/axe-rule-mapping.md
 *
 * Reads the installed axe-core's internal rule registry, extracts each rule's
 * id, impact, tags (including WCAG SCs), description, and help URL, then
 * writes a markdown reference file. Run this after upgrading axe-core to keep
 * the mapping in sync.
 */

import axeModule from 'axe-core';
import { writeFile } from 'node:fs/promises';
import { fileURLToPath } from 'node:url';
import { dirname, resolve } from 'node:path';

const axe = axeModule.default ?? axeModule;

const __dirname = dirname(fileURLToPath(import.meta.url));
const DEFAULT_OUT = resolve(
  __dirname,
  '..',
  'references',
  'axe-rule-mapping.md',
);

function wcagTagsFromRuleTags(tags) {
  // axe uses tags like 'wcag2a', 'wcag143' (SC 1.4.3), 'wcag22aa'
  return tags.filter((t) => /^wcag\d/.test(t));
}

function scIdsFromWcagTags(wcagTags) {
  // 'wcag143' -> '1.4.3' ; 'wcag2a'/'wcag22aa' are level tags, skip
  const scs = [];
  for (const t of wcagTags) {
    const m = t.match(/^wcag(\d)(\d)(\d+)$/);
    if (m) {
      scs.push(`${m[1]}.${m[2]}.${m[3]}`);
    }
  }
  return scs;
}

function levelFromWcagTags(wcagTags) {
  if (wcagTags.includes('wcag2aaa') || wcagTags.includes('wcag21aaa') || wcagTags.includes('wcag22aaa')) {
    return 'AAA';
  }
  if (wcagTags.includes('wcag2aa') || wcagTags.includes('wcag21aa') || wcagTags.includes('wcag22aa')) {
    return 'AA';
  }
  if (wcagTags.includes('wcag2a') || wcagTags.includes('wcag21a') || wcagTags.includes('wcag22a')) {
    return 'A';
  }
  return '-';
}

function isBestPractice(tags) {
  return tags.includes('best-practice');
}

async function main() {
  const outPath = process.argv[2] ?? DEFAULT_OUT;
  const audit = axe._audit;
  const getRulesMeta = axe.getRules();

  // Merge impact (from audit.rules) with description/helpUrl (from getRules)
  const metaById = new Map(getRulesMeta.map((r) => [r.ruleId, r]));
  const rules = audit.rules.map((r) => {
    const meta = metaById.get(r.id) ?? {};
    const wcagTags = wcagTagsFromRuleTags(r.tags);
    const scs = scIdsFromWcagTags(wcagTags);
    return {
      id: r.id,
      impact: r.impact ?? '-',
      level: levelFromWcagTags(wcagTags),
      scs,
      bestPractice: isBestPractice(r.tags),
      description: meta.description ?? '',
      help: meta.help ?? '',
      helpUrl: meta.helpUrl ?? '',
      tags: r.tags,
    };
  });

  // Sort: WCAG-normative first (by SC number), then best-practice (by id)
  rules.sort((a, b) => {
    if (a.bestPractice !== b.bestPractice) return a.bestPractice ? 1 : -1;
    const aKey = a.scs[0] ?? 'zzz';
    const bKey = b.scs[0] ?? 'zzz';
    if (aKey !== bKey) return aKey.localeCompare(bKey);
    return a.id.localeCompare(b.id);
  });

  const wcagRules = rules.filter((r) => !r.bestPractice);
  const bpRules = rules.filter((r) => r.bestPractice);

  const md = renderMarkdown({ axeVersion: axe.version, wcagRules, bpRules });

  await writeFile(outPath, md, 'utf8');
  process.stderr.write(
    `gen-axe-mapping: ${rules.length} rules (${wcagRules.length} WCAG, ${bpRules.length} best-practice) -> ${outPath}\n`,
  );
}

function renderMarkdown({ axeVersion, wcagRules, bpRules }) {
  const lines = [];
  lines.push('<!--');
  lines.push('  AUTO-GENERATED by scripts/gen-axe-mapping.mjs');
  lines.push('  DO NOT EDIT BY HAND. Re-run the script after upgrading axe-core.');
  lines.push('-->');
  lines.push('');
  lines.push('# axe-core rule → WCAG SC mapping');
  lines.push('');
  lines.push(`Generated from axe-core \`${axeVersion}\`.`);
  lines.push('');
  lines.push(
    'Each axe rule maps to one or more WCAG success criteria via its tags. ' +
      "This file is the authoritative mapping used by the skill's scoring " +
      'and report generation.',
  );
  lines.push('');
  lines.push('## WCAG-normative rules');
  lines.push('');
  lines.push(renderTable(wcagRules));
  lines.push('');
  lines.push('## Best-practice rules (non-normative)');
  lines.push('');
  lines.push(
    'These rules catch real accessibility issues but are not tied to any ' +
      'WCAG success criterion. They are excluded from audits by default; ' +
      'pass `--include-best-practice` to include them. They **never** affect ' +
      'VPAT conformance classifications.',
  );
  lines.push('');
  lines.push(renderTable(bpRules));
  lines.push('');
  return lines.join('\n');
}

function renderTable(rules) {
  const header =
    '| Rule ID | Impact | Level | WCAG SCs | Description |\n' +
    '|---|---|---|---|---|';
  const body = rules
    .map((r) => {
      const scs = r.scs.length > 0 ? r.scs.join(', ') : '-';
      const desc = (r.description || r.help || '').replace(/\|/g, '\\|');
      return `| \`${r.id}\` | ${r.impact} | ${r.level} | ${scs} | ${desc} |`;
    })
    .join('\n');
  return header + '\n' + body;
}

main().catch((err) => {
  process.stderr.write(`gen-axe-mapping: ${err.message}\n`);
  process.exit(2);
});
